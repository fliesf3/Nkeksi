package tech.firefly.nkeksi;


public class LocationSearchModel {
    String restoImage;
    String restoName;
    String restoLocation;

    public String getRestoImage() {
        return restoImage;
    }

    public void setRestoImage(String restoImage) {
        this.restoImage = restoImage;
    }

    public String getRestoName() {
        return restoName;
    }

    public void setRestoName(String restoName) {
        this.restoName = restoName;
    }

    public String getRestoLocation() {
        return restoLocation;
    }

    public void setRestoLocation(String restoLocation) {
        this.restoLocation = restoLocation;
    }
}
